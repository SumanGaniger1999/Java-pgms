package com.xworkz.arrayex1app;
//Arrays to toString
import java.util.Arrays;

public class Array {
	public static void main(String[] args) {
        Boolean[] a=new Boolean[] {true,false,true};
         System.out.println(Arrays.toString(a));
        
         Byte[]   byteArr=new Byte[] {10,20,10};
         System.out.println(Arrays.toString(byteArr));
         
         Character[]     charArr=new Character[ ] {'g','e','f'};
         System.out.println(Arrays.toString(charArr));
         
       
         
         Integer[]          intArr=new Integer[]  {1,2,3};
         System.out.println(Arrays.toString(intArr));
         
         Float[]     fltArr=new Float[] {(float)2,(float)4,(float)6};
         System.out.println(Arrays.toString(fltArr));
         
       
         Double[]     dblArr=new Double[] {(double)1,(double)23,(double)3};
         System.out.println(Arrays.toString(dblArr));
         
         Short[]       shortArr=new Short[] {1,2,3,4};
         System.out.println(Arrays.toString(shortArr));
        		 
        		 
        		 
        		 
        		 
        		 
         }
}

